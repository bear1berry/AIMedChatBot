def _fmt_date(dt: Optional[datetime]) -> str:
    """–§–æ—Ä–º–∞—Ç–∏—Ä—É–µ—Ç –¥–∞—Ç—É –≤ —É–¥–æ–±–Ω–æ–º –≤–∏–¥–µ –∏–ª–∏ –≤–æ–∑–≤—Ä–∞—â–∞–µ—Ç '‚Äî'."""
